package com.cgvsu.math.point;

// Класс для описания точки с двумя координатами с public полями (для простой читаемости кода)
// (в частности для работы в папке rasterization)
public class Point2f {
    public float x;
    public float y;

    public Point2f(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
